package st.lab1.task3;
public class Cloud {
    private int hydrogen;
    private int ozone;
    private int carbonOxide;

    public Cloud() {
        this.hydrogen = 1;
        this.ozone = 1;
        this.carbonOxide = 1;
    }

    public int getHydrogen() {
        return hydrogen;
    }

    public int getOzone() {
        return ozone;
    }

    public int getCarbonOxide() {
        return carbonOxide;
    }
}
